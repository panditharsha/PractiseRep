package for_loop;

public class do_while_loop {

	public static void main(String[] args) 
	{
		/// 1--->20
		int i=1;// initialization
		
		do {
			System.out.println("value of i is "+i);
			i++;/// increment
			
		}
		while(i<=20);///condition check 
	
	
	int j=20;
	
	do {
		System.out.println("value of j is "+j);
		j--; //decrement
	}while(j>=1);	
	}
	

}
