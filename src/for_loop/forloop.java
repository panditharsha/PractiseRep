package for_loop;

public class forloop {

	public static void main(String[] args) 
	{
		/// 1--->10
		/// start point 1----> initialization
		/// condition <=10
		//// increment ++, decrement ---
		
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		
		////10----->1
		/// start point 10---> initialization
		///condition >=1
		/// increment/ decrement---->---
		
		for(int j=10 ; j>=1;j--)
		{
			System.out.println(j);
		}

	}

}
