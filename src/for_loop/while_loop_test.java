package for_loop;

public class while_loop_test {

	public static void main(String[] args) {
		System.out.println("increment number");
		int i=2;
		while(i<20)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("decrement number");
		int j=25;
		while(j>1)
		{
			System.out.println(j);
			j--;
		}
		

	}

}
