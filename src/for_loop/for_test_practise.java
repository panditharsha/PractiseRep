package for_loop;

public class for_test_practise {

	public static void main(String[] args) {
		
		System.out.println("increment number");
		for(int i=1;i<15;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("decrement from");
		for(int  j=20;j>1;j--)
		{
			System.out.println(j);
		}
		

	}

}

